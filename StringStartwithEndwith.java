package mypack;

public class StringStartwithEndwith {
	
	public static void main(String[] args) {
		String s1=("CHANDANKUMAR ");
		System.out.println(s1.startsWith("C"));
		System.out.println(s1.endsWith("R"));
		String s2=("kundankumar");
		System.out.println(s2.startsWith("k"));
		System.out.println(s2.endsWith("r"));
		
	}

}
